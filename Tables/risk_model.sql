DROP TABLE risk_model CASCADE CONSTRAINTS;
CREATE TABLE risk_model
(
	model_id	INT NOT NULL,
	name		VARCHAR2(80) NOT NULL,
	description	VARCHAR2(80),
	mnemonic	VARCHAR2(20) NOT NULL,
        numeraire       VARCHAR2(3) NOT NULL,
        model_region    VARCHAR2(32),
	PRIMARY KEY (model_id)
) TABLESPACE gmdl_meta;
